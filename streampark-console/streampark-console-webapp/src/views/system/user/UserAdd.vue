<template>
  <a-drawer
    :mask-closable="false"
    width="650"
    placement="right"
    :closable="true"
    @close="onClose"
    :visible="visible"
    style="height: calc(100% - 55px);overflow: auto;padding-bottom: 53px;">
    <template slot="title">
      <a-icon type="user" />
      Add User
    </template>
    <a-form
      :form="form">
      <a-form-item
        label="User Name"
        v-bind="formItemLayout"
        :validate-status="validateStatus"
        :help="help">
        <a-input
          @blur="handleUserNameBlur"
          v-decorator="
            ['username',
             {rules: [{ required: true }]}]" />
      </a-form-item>
      <a-form-item
        label="Nick Name"
        v-bind="formItemLayout"
        :validate-status="validateStatus"
        :help="help">
        <a-input
          v-decorator="['nickName',{rules: [{ required: true }]}]" />
      </a-form-item>
      <a-form-item
        label="Password"
        v-bind="formItemLayout">
        <a-input-password
          placeholder="input password"
          v-decorator="['password',{rules: [
            { required: true, message: 'password is required'},
            { min: 8, message: 'Password length cannot be less than 8 characters'}
          ]}]" />
      </a-form-item>
      <a-form-item
        label="E-Mail"
        v-bind="formItemLayout">
        <a-input
          v-decorator="['email',{rules: [
            { type: 'email', message: 'please enter a valid email address' },
            { max: 50, message: 'exceeds maximum length limit of 50 characters'}
          ]}]" />
      </a-form-item>
      <a-form-item
        label="Description"
        v-bind="formItemLayout">
        <a-input
          v-decorator="['description',{rules: [
            { max: 100, message: 'exceeds maximum length limit of 100 characters'}
          ]}]" />
      </a-form-item>
      <a-form-item
        label="Role"
        v-bind="formItemLayout">
        <a-select
          mode="multiple"
          :allow-clear="true"
          style="width: 100%"
          v-decorator="['roleId',{rules: [{ required: true, message: 'please select role' }]}]">
          <a-select-option
            v-for="r in roleData"
            :key="r.roleId">
            {{ r.roleName }}
          </a-select-option>
        </a-select>
      </a-form-item>
      <a-form-item
        label="Status"
        v-bind="formItemLayout">
        <a-radio-group
          v-decorator="['status',{rules: [{ required: true, message: 'please select status'}]}]">
          <a-radio
            value="0">
            locked
          </a-radio>
          <a-radio
            value="1">
            effective
          </a-radio>
        </a-radio-group>
      </a-form-item>
      <a-form-item
        label="Gender"
        v-bind="formItemLayout">
        <a-radio-group
          v-decorator="['sex',{rules: [{ required: true, message: 'please select gender' }]}]">
          <a-radio
            value="0">
            male
          </a-radio>
          <a-radio
            value="1">
            female
          </a-radio>
          <a-radio
            value="2">
            secret
          </a-radio>
        </a-radio-group>
      </a-form-item>
    </a-form>
    <div
      class="drawer-bootom-button">
      <a-button
        @click="onClose">
        Cancel
      </a-button>
      <a-button
        @click="handleSubmit"
        type="primary"
        :loading="loading">
        Submit
      </a-button>
    </div>
  </a-drawer>
</template>
<script>
import { list as getRole } from '@/api/role'
import { checkUserName, post } from '@/api/user'

const formItemLayout = {
  labelCol: { span: 4 },
  wrapperCol: { span: 18 }
}
export default {
  name: 'UserAdd',
  props: {
    visible: {
      type: Boolean,
      default: false
    }
  },
  data () {
    return {
      loading: false,
      roleData: [],
      formItemLayout,
      form: this.$form.createForm(this),
      validateStatus: '',
      help: ''
    }
  },
  methods: {
    reset () {
      this.validateStatus = ''
      this.help = ''
      this.loading = false
      this.form.resetFields()
    },

    onClose () {
      this.reset()
      this.$emit('close')
    },

    handleSubmit() {
      this.form.validateFields((err, user) => {
        if (!err && this.validateStatus === 'success') {
          user.roleId = user.roleId.join(',')
          post({
            ...user
          }).then((r) => {
            if (r.status === 'success') {
              this.reset()
              this.$emit('success')
            }
          }).catch(() => {
            this.loading = false
          })
        }
      })
    },
    handleUserNameBlur (e) {
      const username = (e && e.target.value) || ''
      if (username.length) {
        if (username.length > 20) {
          this.validateStatus = 'error'
          this.help = 'User name should not be longer than 20 characters'
        } else if (username.length < 4) {
          this.validateStatus = 'error'
          this.help = 'Team name should not be less than 4 characters'
        } else {
          this.validateStatus = 'validating'
          checkUserName({
            username: username
          }).then((r) => {
            if (r.data) {
              this.validateStatus = 'success'
              this.help = ''
            } else {
              this.validateStatus = 'error'
              this.help = 'Sorry, the user name already exists'
            }
          })
        }
      } else {
        this.validateStatus = 'error'
        this.help = 'User name cannot be empty'
      }
    }
  },
  watch: {
    visible () {
      if (this.visible) {
        getRole(
          { 'pageSize': '9999' }
        ).then((resp) => {
          this.roleData = resp.data.records
        })
      }
    }
  }
}
</script>
